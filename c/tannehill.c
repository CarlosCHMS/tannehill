double gamma_a1(double* a, double Y, double Z)
{

    double gamma = a[0] + a[1]*Y + a[2]*Z;
    gamma += a[3]*Y*Z + a[4]*Y*Y + a[5]*Z*Z;
    gamma += a[6]*Y*Z*Z + a[7]*Z*Z*Z;

    double aux = a[8] + a[9]*Y + a[10]*Z + a[11]*Y*Z;
    aux /= 1 + exp((a[12] + a[13]*Y)*(Z + a[14]*Y + a[15]));

    gamma += aux;

    return gamma;

}

double gamma_a(GAS* gas, double rho, double e)
{

    double Y = log10(rho/1.292);
    double Z = log10(e/78408.4);

    double* a = gasFuncA(gas, Y, Z);
        
    return gamma_a1(a, Y, Z);

}

double gasSound(GAS* gas, double rho, double e)
{

    double aux = log(10);

    double Y = log10(rho/1.292);
    double Z = log10(e/78408.4);

    double* a = gasFuncA(gas, Y, Z);

    double g0 = gamma_a1(a, Y, Z);
    
    double g1 = gamma_a1(a, Y + 0.01, Z);
    double dgdy = (g1-g0)/(0.01);

    g1 = gamma_a1(a, Y, Z + 0.01);
    double dgdz = (g1-g0)/(0.01);
    
    double K1 = a[16];
    double K2 = a[17];
    double K3 = a[18];
    
    return sqrt(e*(K1 + (g0-1)*(g0 + K2*dgdz/aux) + K3*dgdy/aux));
    
}

double gasPressureAux(GAS* gas, double rho, double e)
{
    return rho*e*(gamma_a(gas, rho, e) - 1);
}

double temperature_b(double* b, double Y, double Z)
{

    double ans = b[0] + b[1]*Y + b[2]*Z;
    ans += b[3]*Y*Z + b[4]*Z*Z + b[5]*Y*Y;
    ans += b[6]*Y*Y*Z + b[7]*Y*Z*Z;
    
    double aux = b[8] + b[9]*Y + b[10]*Z + b[11]*Y*Z + b[12]*Z*Z;
    aux /= 1 + exp((b[13]*Y + b[14])*(Z + b[15]));

    ans += aux;

    return ans;

}

double gamma_c1(double* c, double X, double Y, double Z)
{

    double ans = c[0] + c[1]*Y + c[2]*Z + c[3]*Y*Z;
    
    double aux = c[4] + c[5]*Y + c[6]*Z + c[7]*Y*Z;
    
    aux /= 1 + exp(c[8]*(X + c[9]*Y + c[10]));
    
    ans += aux;
    
    return ans;
}

double gamma_c(GAS* gas, double rho, double p)
{ 

    double Y = log10(rho/1.292);
    double X = log10(p/1.013e5);
    double Z = X-Y;
    
    double* c = gasFuncC(gas, Y, Z);
    
    return gamma_c1(c, X, Y, Z);

}

double gasEntalpy(GAS* gas, double rho, double p)
{
    
    double g = gamma_c(gas, rho, p);
    
    return (p/rho)*(g/(g-1));
    
}

double gasEnergy(GAS* gas, double rho, double p)
{
    
    return gasEntalpy(gas, rho, p) - p/rho;
    
}


double gasPressure(GAS* gas, double rho, double e)
{

    double p = gasPressureAux(gas, rho, e);
    
    double dp = p*0.001;
    
    double e1 = gasEnergy(gas, rho, p);
    double e2 = gasEnergy(gas, rho, p+dp);
    
    p += (e - e1)*dp/(e2-e1);

    
    e1 = gasEnergy(gas, rho, p);
    e2 = gasEnergy(gas, rho, p+dp);
    
    p += (e - e1)*dp/(e2-e1);
    

    return p; 

}

double gasTemperature(GAS* gas, double rho, double e)
{


    double p = gasPressure(gas, rho, e);

    double Y = log10(rho/1.225);
    double X = log10(p/1.0134e5);
    double Z = X-Y;

    double* b = gasFuncB(gas, Y, Z);    
    
    double T;
        
    if(b[0] == 0.0)
    {
        T = p/(287.058*rho);
    }
    else
    {
        T = 151.78*pow(10.0, temperature_b(b, Y, Z));
    }
    
    return T;
    
}

